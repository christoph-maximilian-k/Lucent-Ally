  /// This method gives indication if a given topLevelDomain was found.
  static bool topLevelDomainFound({required String topLevelDomain}) {
    for (final CountrySpecification item in countrySpecifications) {
      if (item.topLevelDomain.toLowerCase() == topLevelDomain.toLowerCase()) return true;
    }

    return false;
  }

  /// This method gives indication if a given international prefix was found.
  static bool internationalPrefixFound({required String prefix}) {
    for (final CountrySpecification item in countrySpecifications) {
      if (item.internationalPhonePrefix.toLowerCase() == prefix.toLowerCase()) return true;
    }

    return false;
  }

  /// This method can be used to access a CountrySpecification by isoCountryCode.
  /// * Returns ```null``` if isoCountryCode was not found or isEmpty.
  static CountrySpecification? getCountrySpecificationByIsoCountryCode({required String isoCountryCode}) {
    // A country code was not supplied.
    if (isoCountryCode.isEmpty) return null;

    // Go through specifications.
    for (final CountrySpecification element in countrySpecifications) {
      if (element.isoCountryCode.toLowerCase() == isoCountryCode.toLowerCase()) return element;
    }

    return null;
  }

  /// This method can be used to access a CountrySpecification by isoCurrencyCode.
  /// * Returns ```null``` if isoCurrencyCode was not found or isEmpty.
  static CountrySpecification? getCountrySpecificationByIsoCurrencyCode({required String isoCurrencyCode}) {
    // A country code was not supplied.
    if (isoCurrencyCode.isEmpty) return null;

    // Go through specifications.
    for (final CountrySpecification element in countrySpecifications) {
      if (element.currencyCode.toLowerCase() == isoCurrencyCode.toLowerCase()) return element;
    }

    return null;
  }

  /// This method can be used to access a CountrySpecification by `languageLocale`.
  /// * Returns ```null``` if no matching locale is found.
  static CountrySpecification? getCountrySpecificationByLocale({required Locale locale}) {
    // Helpers.
    final String languageCode = locale.languageCode;
    final String? countryCode = locale.countryCode;

    // A country code was not supplied.
    // * Try and find language locale.
    if (countryCode == null || countryCode.isEmpty) {
      // No langauge locale found.
      if (languageCode.isEmpty) return null;

      // Go through specifications.
      for (final CountrySpecification element in countrySpecifications) {
        if (element.languageCode.toLowerCase() == languageCode.toLowerCase()) return element;
      }

      return null;
    }

    // * A country code was found but language code is empty.
    if (languageCode.isEmpty) {
      // Go through specifications.
      for (final CountrySpecification element in countrySpecifications) {
        if (element.isoCountryCode.toLowerCase() == countryCode.toLowerCase()) return element;
      }

      return null;
    }

    // * Bot variables were found, create search variable.
    final String searchTerm = "$languageCode-$countryCode";

    // Go through specifications.
    for (final CountrySpecification element in countrySpecifications) {
      // Turn into lower case.
      final List<String> lowerCaseList = element.languageLocales.map((s) => s.toLowerCase()).toList();

      // Search for term.
      if (lowerCaseList.contains(searchTerm.toLowerCase())) return element;
    }

    return null;
  }

