  /// This method can be used to access countrySpecifications filtered by currency.
  static List<CountrySpecification> get getCurrencies {
    // Init helper list.
    List<CountrySpecification> currencies = [];

    // Helper for more efficient sorting.
    List<String> currencyCodes = [];

    for (final CountrySpecification specification in countrySpecifications) {
      // Check for duplicate.
      if (currencyCodes.contains(specification.currencyCode)) continue;

      // Add relevant data.
      currencies.add(specification);
      currencyCodes.add(specification.currencyCode);
    }

    return currencies;
  }

